    private void confirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmButtonActionPerformed
        // get customer address for shipping label then print it
        String sql = "select fname, lname, street, unit, city, state, zip, ship_method from customers c, address a, orders o " +
                    "where o.order_num = " + orderNum.toString() + " and o.customer_num = c.customer_num and a.customer_num = c.customer_num;";
        
        String label = "Ship to:\n";
        try {
            ResultSet rs = SQL.executeSystemSQLQuery(sql);
            if(rs.next()) {
                label +=  rs.getString(1) + " " + rs.getString(2)
                        + "\n" + rs.getString(3) + "\n" + rs.getString(5) + ", " + rs.getString(6)
                        + " " + rs.getString(7) + "\n\nShipping method: ";
                
                switch(rs.getInt("ship_method"))
                {
                    case 1: label += "Standard";
                        break;
                    case 2: label += "Express";
                        break;
                    case 3: label += "Overnight";                    
                }
            }
        } catch (SQLException ex) {            
            Logger.getLogger(ShipOrderDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        PrintRequestAttributeSet attr = new HashPrintRequestAttributeSet();
        attr.add(MediaSizeName.ISO_A10);
        JTextArea jt = new JTextArea(label);
        try {
            statusChanged = jt.print();           
        } catch (PrinterException ex) {
            Logger.getLogger(ShipOrderDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        reduceInventory();
        dispose();
    }//GEN-LAST:event_confirmButtonActionPerformed

