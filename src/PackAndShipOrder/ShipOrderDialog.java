    private void reduceInventory() {
        DefaultTableModel model = (DefaultTableModel)orderTable.getModel();
        int rows = model.getRowCount();
        for(int i = 0; i < rows; i++) {
            try {
                SQL.executeSystemSQLUpdate("update inventory set on_hand = on_hand - "
                        + model.getValueAt(i, 2) + " where product_num = " + model.getValueAt(i, 0));
            } catch (SQLException ex) {
                Logger.getLogger(ShipOrderDialog.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
